async function main() {
  const rows: Row[] = [
    ...KOSPI.map(([ticker, name]) => ({ ticker, name, market: "KRX" as const, currency: "KRW" as const, is_overseas_etf: false })),
    ...KOSDAQ.map(([ticker, name]) => ({ ticker, name, market: "KRX" as const, currency: "KRW" as const, is_overseas_etf: false })),
    ...ETF_KRX
      .filter(([ticker]) => ticker !== "환매") // placeholder 제거
      .map(([ticker, name, isOverseas]) => ({ ticker, name, market: "KRX" as const, currency: "KRW" as const, is_overseas_etf: isOverseas as boolean })),
  ];

  // 중복 ticker 제거
  const seen = new Set<string>();
  const deduped = rows.filter(r => {
    if (seen.has(r.ticker)) return false;
    seen.add(r.ticker);
    return true;
  });

  console.log(`총 ${deduped.length}개 종목 upsert 시작...`);
  console.log(`  KOSPI:        ${KOSPI.length}`);
  console.log(`  KOSDAQ:       ${KOSDAQ.filter(([t]) => t !== "환매").length}`);
  console.log(`  ETF (국내):   ${ETF_KRX.filter(([t, , o]) => !o && t !== "환매").length}`);
  console.log(`  ETF (해외투자): ${ETF_KRX.filter(([t, , o]) => !!o && t !== "환매").length}`);

  const BATCH = 50;
  for (let i = 0; i < deduped.length; i += BATCH) {
    const batch = deduped.slice(i, i + BATCH);
    const { error } = await supabase
      .from("securities")
      .upsert(batch, { onConflict: "ticker,market", ignoreDuplicates: false });
    if (error) { console.error(`배치 오류:`, error.message); throw error; }
    process.stdout.write(`\r  [${i + batch.length}/${deduped.length}] 완료`);
  }
  console.log("\n✅ 시딩 완료!");
}

main().catch((e) => { console.error(e); process.exit(1); });
